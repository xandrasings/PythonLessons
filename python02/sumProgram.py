def main():
    #ask the user to input integers
    integer_1 = eval(input("Please give me a number. "))
    
    integer_2 = eval(input("Please enter a second number. "))
    
    integer_3 = eval(input("One more number please. "))
    
    #define the sum
    sum = integer_1 + integer_2 + integer_3
    
    #print the sum
    print("The total of your numbers is ")
    print(sum)

main()

